                    <b class="text-lg">Los pescadores y la Virgen: Raíces de la devoción en Anzoátegui</b>
                    <br>
                    <p>
                        Según la historia, la veneración a la Virgen inició en Anzoátegui cuando llegaron a tierra
                        firme los pescadores margariteños en los siglos XIX y XX, encontrando en ella refugio
                        espiritual y una guia segura en sus viajes. Con el pasar de los años, la devoción se arraigó
                        en el corazón de los habitantes de Lechería.
                    </p>
                    <p>
